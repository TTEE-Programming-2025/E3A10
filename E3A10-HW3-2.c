void showSeats(char temp[ROWS][COLS]) {
    printf("\\123456789\n");
    for (int i = 0; i < ROWS; i++) {
        printf("%d-", 9 - i);  // 從第9排數到第1排
        for (int j = 0; j < COLS; j++) {
            if (temp != NULL && temp[i][j] == '@')
                printf("@");
            else
                printf("%c", seats[i][j]);
        }
        printf("\n");
    }
}
