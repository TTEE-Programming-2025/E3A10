// 密碼檢查，最多三次
int pw() {
    int pw = 2025;
    int input, a = 0;

    while (a < 3) {
        printf("\n請輸入 4 位數密碼 (提示：2025)：");
        scanf("%d", &input);

        if (input == pw) {
            printf("密碼正確，進入系統...\n");
            return 1;
        } else {
            printf("密碼錯誤！\n");
            a++;
        }
    }
    return 0;
}

char seats[ROWS][COLS]; // '-'空位，'*'已預約，'@'暫選

void initSeats() {
    for (int i = 0; i < ROWS; i++)
        for (int j = 0; j < COLS; j++)
            seats[i][j] = '-';

    // 固定設定10個已預約座位（模擬預約）
    int fixed[10][2] = {
        {0, 1}, {1, 8}, {3, 5}, {4, 3}, {4, 8},
        {6, 3}, {6, 4}, {7, 3}, {8, 1}, {8, 2}
    };

    for (int i = 0; i < 10; i++) {
        int r = fixed[i][0];
        int c = fixed[i][1];
        seats[r][c] = '*';
    }
}

