// 處理使用者輸入的字元
void C() {
    char ch;
    printf("請輸入一個字元: ");
    fflush(stdin);
    scanf("%c", &ch);

    if (ch >= 'A' && ch <= 'Z') {
        printf("Uppercase\n");
    } else if (ch >= 'a' && ch <= 'z') {
        printf("Lowercase\n");
    } else if (ch >= '0' && ch <= '9') {
        printf(" Digit\n");
    } else {
        printf("E3A 10 陳品元\n");
    }
    
    getchar();
}

